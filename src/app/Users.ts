export class Users
{
       id:any="";
      firstname: any="";
      lastname:any ="";
      email:any="";
      mobile:any="";
      salary:any="";

      constructor(id:any,firstname:any,lastname:any,email:any,mobile:any,salary:any)
      {
              this.id=id;
              this.firstname =firstname;
              this.lastname = lastname;
              this.mobile =mobile;
              this.email = email;
              this.salary = salary;
      }
}
